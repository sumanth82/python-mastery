# Essentially helps to organize code
# module naming convention - snake_case

# One of the common ways to utilize a module is using utility - simple functions that can be used all across the projects:
# structure:

# /module
#   - main.py           - import utility
#   - utility.py        - Define common functions

# NOTE: When you import a module, it generates a __pycache folder; it caches the imported utility to load compiled utility
